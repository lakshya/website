The right to information is fundamental in bolstering democratic principles of openness, transparency and accountability in societies and in eradicating poverty and is therefore a programming priority for United Nations Development Program.<br/>
<br/>
Asthra is the name given to the project which aims to create a society free of corruption through the Right to Information act. This was started at NIT Warangal, and through it, we aim to <br/>
<br/>

<ol><li>Spread awareness about the RTI act </li><li>File RTIs for issues we take up </li>
  <li>Help others file RTIs </li>
</ol>
<p>Ultimately, every citizen should be an RTI activist.</p>
