<p>In India, there are 456 million Indians living below the poverty line with limited or no access to the bare necessities like food, clothing and shelter.</p>
<ol>
  <li>&quot;Ek Jodi Kapda&quot;, literally meaning &quot;a set of clothes&quot;, is an initiative started by Goonj in association with Whirlpool. </li>
  <li>As a pilot project Lakshya will collect as many clothes, shoes, and anything useful for a family, as possible, and give it to those who need it the most. </li>
  <li>Our unused clothes will bring much needed cheer and relief for another person in need of the same.</li>
</ol>
