<br/>
<b>Moving from grant to loan model</b>
<p>
With the increase in institute fees from around Rs.19,000 p.a. in 2004 to Rs.45,000 p.a. in 2010, fund raising is a challenge which gets tougher every year. To make this project sustainable, the Lakshya team has been working on a loan based model.
<br/>
<br/>
The key points are:
<br/>
<ul>
<li>Lakshya will move from giving grants to giving loans.</li>
<li>The loans will be <b>interest free (0% interest) and no collateral will be required</b>.</li>
<li>The loan period will be fixed at 5 years from graduation. Time spent for higher studies will not be counted as part of the 5 years.</li>
<li>To keep track of all scholars over 5 years, each scholar will have to pay Rs.100 every month as soon as he/she graduates. This will be mandatory.</li>
</ul>
<br/>
The advantages of having such a system are:
<ul>
<li>It is in the interest of our long term goal of making this project sustainable.</li>
<li>Over time, it will reduce the burden on fund-raising.</li>
<li>Coupled with donations, it will help accomodate more scholars every year.</li>
</ul>
</p>

